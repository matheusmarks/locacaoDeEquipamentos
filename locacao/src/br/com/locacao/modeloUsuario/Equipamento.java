package br.com.locacao.modeloUsuario;

public class Equipamento {
	private int cod_equipamentos;
	private String nome_equipamento;
	private String descricao;
	
	public int getCod_equipamentos() {
		return cod_equipamentos;
	}
	
	public void setCod_equipamentos(int cod_equipamentos) {
		this.cod_equipamentos = cod_equipamentos;
	}
	
	public String getNome_equipamento() {
		return nome_equipamento;
	}
	
	public void setNome_equipamento(String nome_equipamento) {
		this.nome_equipamento = nome_equipamento;
	}
	
	public String getDescricao() {
		return descricao;
	}
	
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	
	
	
	
}
